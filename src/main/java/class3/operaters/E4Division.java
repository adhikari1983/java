package class3.operaters;

public class E4Division {
    public static void main(String[] args) {
        int number1 = 20;
        int number2 = 10;
        int division = number1/number2;
        System.out.println(10 / 20);
        System.out.println(number1 / number2);
        System.out.println(division);

        System.out.println(10/5); //if the data type is int we will not see the decimal.
        System.out.println(11/5); //if the data type is float/double we will be able to see/store the decimal.
        System.out.println(2.0/1.0);



    }
}
