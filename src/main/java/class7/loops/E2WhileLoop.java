package class7.loops;

public class E2WhileLoop {
    public static void main(String[] args) {

        int number = 0;
        number++; // 0 -> 1 using the increment operator
        number++; // 1 -> 2
        System.out.println(number);

        number--; // 2 ->  1
        number--; // 1 ->  0
        number--; // 0 -> -1
        System.out.println(number);
    }
}
