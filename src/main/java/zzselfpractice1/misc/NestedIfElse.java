package zzselfpractice1.misc;

public class NestedIfElse {
    public static void main(String[] args) {
        int num = 15;
        if (num > 10) {
            System.out.println(" >10");
        } else if (num == 10) {
            System.out.println("=10");
        } else {
            System.out.println("<10");
        }
    }
}
