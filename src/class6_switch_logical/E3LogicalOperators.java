package class6_switch_logical;

public class E3LogicalOperators {
    public static void main(String[] args) {
        boolean rich = true;
        boolean invitationFromRR = false;

        if (rich && invitationFromRR) {
            System.out.println("You can buy Royals Royce");
        } else {
            System.out.println("You can't buy Royals Royce");
        }
    }
}
